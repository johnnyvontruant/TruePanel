"""
Custom LCD glyph definitions and loader contract.

The A125 transport command for CGRAM programming is not yet verified, so the
loader refuses to send data until a supported hardware backend implements
define_custom_character().
"""

from __future__ import annotations

from .glyphs import CustomGlyphUnavailable


VERTICAL_BARS = (
    (0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00),
    (0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x1F),
    (0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x1F, 0x1F),
    (0x00, 0x00, 0x00, 0x00, 0x00, 0x1F, 0x1F, 0x1F),
    (0x00, 0x00, 0x00, 0x00, 0x1F, 0x1F, 0x1F, 0x1F),
    (0x00, 0x00, 0x00, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F),
    (0x00, 0x00, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F),
    (0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F, 0x1F),
)


def load_vertical_bar_glyphs(lcd):
    define = getattr(lcd, "define_custom_character", None)

    if not callable(define):
        raise CustomGlyphUnavailable(
            "The active LCD backend cannot program custom characters"
        )

    for slot, bitmap in enumerate(VERTICAL_BARS):
        define(slot, bitmap)

    return True
